library(caTools)
myData=read.csv("housing.csv",stringsAsFactors = F)
print(myData)
class(myData)
structure(myData)
summary(myData)
split=sample.split(Y=myData$MEDV,SplitRatio=0.7)
training_set=subset(x=myData,split==TRUE)
testing_set=subset(x=myData,split==FALSE)
reg=lm(formula = MEDV~.,data=training_set)
summary(reg)
plot(reg)
y_pred=predict(object=reg,newData=testing_set)
print(y_pred)
sqrt(sum((y_pred-testing_set$MEDV)^2)/length(testing_set$MEDV))
summary(reg)$r.squared
